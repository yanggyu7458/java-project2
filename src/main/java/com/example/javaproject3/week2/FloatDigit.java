package com.example.javaproject3.week2;

public class FloatDigit {
    public static void main(String[] args) {
        float f2 = 1.23e5f;
        float f3 = 1.23e6f;
        float f4 = 1.23e7f;
        float f5 = 1.23e8f;
        float f6 = 1.23e9f;
        float f7 = 1.23e10f;
        double d10 = 1.23e10;

        System.out.printf("f2 : %f\nf3 : %f\nf4 : %f\nf5 : %f\nf6 : %f\nf7 : %f\n", f2, f3, f4, f5, f6, f7);
        System.out.printf("d10 : %f\n", d10);
    }
}

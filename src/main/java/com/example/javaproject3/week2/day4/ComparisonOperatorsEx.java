package com.example.javaproject3.week2.day4;

public class ComparisonOperatorsEx {
    public static void main(String[] args) {
        int iVal1 = 10;
        int iVal2 = 20;
        //boolean result = iVal1 == iVal2
        System.out.printf("a와 b가 같은지? %b", iVal1 == iVal2);
        System.out.printf("a와 b가 같은지? %b", iVal1 != iVal2);
        System.out.printf("a와 b가 같은지? %b", iVal1 < iVal2);
        System.out.printf("a와 b가 같은지? %b", iVal1 <= iVal2);
        System.out.printf("a와 b가 같은지? %b", iVal1 > iVal2);
        System.out.printf("a와 b가 같은지? %b", iVal1 >= iVal2);
    }
}
